"""LLM-as-Judge report evaluation domain model.

Stores post-finalize quality scores so we can track report quality
trends without relying on human review of every Markdown artifact.
"""

from __future__ import annotations

from datetime import datetime

from pydantic import Field

from app.domain.base import ConnorBaseModel, NonEmptyStr, utc_now


class ReportEvaluation(ConnorBaseModel):
    """Multi-dimensional quality score for a finalized daily report.

    Generated by an independent judge model call after the writing
    loop finalizes a report.  Each dimension is 0–10.
    """

    id: NonEmptyStr
    report_id: NonEmptyStr
    run_id: NonEmptyStr
    evaluated_at: datetime = Field(default_factory=utc_now)

    # ---- seven quality dimensions (0–10) ----
    accuracy: float = Field(default=0, ge=0, le=10)
    completeness: float = Field(default=0, ge=0, le=10)
    prudence: float = Field(default=0, ge=0, le=10)
    structure: float = Field(default=0, ge=0, le=10)
    readability: float = Field(default=0, ge=0, le=10)
    actionability: float = Field(default=0, ge=0, le=10)
    finance_quality: float | None = Field(default=None, ge=0, le=10)

    # ---- aggregate ----
    total_score: float = Field(default=0, ge=0, le=10)
    critical_issues: list[str] = Field(default_factory=list)
    suggestions: list[str] = Field(default_factory=list)
    reasoning: str = ""

    # ---- metadata ----
    judge_model: str = ""
    judge_duration_ms: int = 0
    schema_version: int = 1
